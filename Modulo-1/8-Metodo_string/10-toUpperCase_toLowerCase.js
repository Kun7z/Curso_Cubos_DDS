/*

toUpperCase()
Coloca todas as letras de
uma determinada string
em maiúsculas.

O método toLowerCase() é
o oposto, e coloca todas as
letras de uma string em
minúsculas.

Exemplo:
*/

const texto = 'Eu amo a Cubos Academy, amo muito!';

const gritando = texto.toUpperCase();

const sussurrando = texto.toLowerCase();


console.log(gritando);

console.log(sussurrando);